namespace CalculatorShell.Base
{
    /// <summary>
    /// Virtual terminal text formatting
    /// </summary>
    public enum TextFormat
    {
        /// <summary>
        /// No formatting aplied
        /// </summary>
        None,
        /// <summary>
        /// Text will be displayed bold
        /// </summary>
        Bold,
        /// <summary>
        /// Text with underline
        /// </summary>
        Underline,
        /// <summary>
        /// Text with strikethrough
        /// </summary>
        Strikethrough,
        /// <summary>
        /// Italic text
        /// </summary>
        Italic,
    }
}
